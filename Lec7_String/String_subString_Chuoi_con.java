package Lec7_String;

public class String_subString_Chuoi_con {

    public static void main(String[] args) {
        String s = "Hello World";
        System.out.println(s.substring(6)); // trich chuoi con tu vi tri (index)so 6 den het
        System.out.println(s.substring(0,5)); // trich chuoi con tu vi tri (index) so 0 den so 5
        System.out.println(s.substring(1,3)); // tuong tu ben tren tu vi tri (index ) so 1 den so 3
    }
    
}
