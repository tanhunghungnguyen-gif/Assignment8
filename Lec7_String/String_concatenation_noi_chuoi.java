package Lec7_String;

public class String_concatenation_noi_chuoi {

    public static void main(String[] args) {
        String s1 = 50 + 30 + "Hello" + 40 + 40; // so + so = so + chuoi + so = chuoi
        String s2 = "Hello"+"World" ;
        System.out.println(s1);
        System.out.println(s2);
    }

}
